    private void setTableModelPeminjaman(){
       
    String[] columnNames = {
        "ID Peminjaman",
        "Tanggal Pinjam",
        "Tanggal Kembali",
        "Nama Anggota",
        "ID Buku",
        "Judul Buku",
        "Jumlah Pinjam",
        "Status Pinjam"
    };

    DefaultTableModel model = new DefaultTableModel(columnNames, 0); // 0 = jumlah baris awal
    jTable1.setModel(model); // Atur model baru ke tabel

    }
    
    private void getDataPeminjaman(DefaultTableModel model){
        model.setRowCount(0);
        
        try { 
        String sql = "SELECT pmd.id_peminjaman, pmj.tgl_peminjaman, " +
                     "pmj.tgl_pengembalian, pmj.nama_anggota, bk.id_buku, " +
                     "bk.judul_buku, pmd.jumlah_pinjam, pmd.status_peminjaman " +
                     "FROM detail_peminjaman pmd " +
                     "INNER JOIN peminjaman pmj ON pmj.id_peminjaman = pmd.id_peminjaman " +
                     "INNER JOIN buku bk ON bk.id_buku = pmd.id_buku";
        
        PreparedStatement st = conn.prepareStatement(sql);
        ResultSet rs = st.executeQuery();
        
        while (rs.next()) {
            String idPeminjaman = rs.getString("id_peminjaman");
            String tanggalPeminjaman = rs.getString("tgl_peminjaman");
            String tanggalPengembalian = rs.getString("tgl_pengembalian");
            String namaAnggota = rs.getString("nama_anggota");
            String idBuku = rs.getString("id_buku");
            String judulBuku = rs.getString("judul_buku");
            String jumlahPinjam = rs.getString("jumlah_pinjam");
            String statusPinjam = rs.getString("status_peminjaman");
            
                Object[] rowData = {idPeminjaman, tanggalPeminjaman, tanggalPengembalian,
                                namaAnggota, idBuku, judulBuku, jumlahPinjam, statusPinjam};
            model.addRow(rowData);
            }
                    
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
