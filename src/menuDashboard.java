    private int jumlahAnggota(){
        int totalAnggota = 0;
        
        try {
            String sql = "SELECT COUNT(*) AS total FROM anggota";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            if(rs.next()){
                totalAnggota = rs.getInt("total");
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return totalAnggota;
    }
    
